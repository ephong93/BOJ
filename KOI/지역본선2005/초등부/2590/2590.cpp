#include <iostream>
#include <algorithm>


using namespace std;

int main() {
    int a[6];
    cin >> a[0] >> a[1] >> a[2] >> a[3] >> a[4] >> a[5];
    int n = 0;
    n = a[5];
    a[5] = 0;

    n += a[4];
    a[0] = max(0, a[0] - a[4]*11);
    a[4] = 0;

    n += a[3];
    if (a[1] < a[3]*5) {
        a[0] = max(0, a[0] - (a[3]*5-a[1])*4);
    }
    a[1] = max(0, a[1] - a[3]*5);
    a[3] = 0;
    
    if (a[2] >= 4) {
        n += a[2]/4;
        a[2] = a[2]%4;
    }
    
    if (a[2] == 3) {
        if (a[1] > 0) {
            a[1]--;
            a[0] = max(0, a[0]-5);
        } else {
            a[0] = max(0, a[0]-9);
        }
        a[2] = 0;
        n++;
    } else if(a[2] == 2) {
        if (a[1] >= 3) {
            a[1] -= 3;
            a[0] = max(0, a[0]-6);
        } else {
            a[0] = max(0, a[0]-(18-a[1]*4));
            a[1] = 0;
        }
        n++;
        a[2] = 0;
    } else if(a[2] == 1) {
        if (a[1] >= 5) {
            a[0] = max(0, a[0]-(36-9-20));
            a[1] -= 5;
        } else {
            a[0] = max(0, a[0]-(36-9-a[1]*4));
            a[1] = 0;
        }
        a[2] = 0;
        n++;
    }

    if(a[1] >= 9) {
        n += a[1]/9;
        a[1] = a[1]%9;
    }

    if (a[1] > 0) {
        a[0] = max(0, a[0]-(36-a[1]*4));
        a[1] = 0;
        n++;
    }

    if (a[0] >= 36) {
        n += a[0]/36;
        a[0] = a[0]%36;
    }
    if (a[0] > 0) {
        n++;
        a[0] = 0;
    }

    cout << n;

    return 0;
}
